package newpackagevariables;

public class EscapeSequence {
	public static void main(String[]args){
		
		// This is the example of a new line character \n
		System.out.println("Good aftrnoon\n This is Kiran");
		
		System.out.println(" How are you\n I am fine");
		
		//This is an example of another way of new line character\n
		
		System.out.println("Good aftrnoon ");
		System.out.println("This is Kiran ");
		System.out.println("How are you"); 
		System.out.println("I am fine");
		
		//Basic formating with tabs
		System.out.println("1\tOne\tCo13");     
		System.out.println("1\tOne\tCo13");
		System.out.println("10\tTen\tCo13");
		System.out.println("100\tOne Hundred\tCo13");
		
		
		
		
		
		
	}

}
